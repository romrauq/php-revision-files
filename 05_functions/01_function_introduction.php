<?php
	# A function is a block of code that can be repeated called/executed.
	# User defined functions are functions that are created by the user/developer.
	
	/*
	Naming conventions for function names:
	Camel case: myFunction()
	Lower case: my_function()
	Pascal case: MyFunction() (Usually used to name classes in OOP)
	*/

	// Defining/Creating a function:
	function simpleFunction(){
	//  This function echoes a specified text whenenever the function is executed/called.
		echo "Hello World!";
		echo "<br>";
	}

	// Executing/calling/running the function:
	simpleFunction();

?>